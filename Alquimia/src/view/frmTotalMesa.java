    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        switch (this.jComboBox1.getSelectedIndex()){
            case 0: //LIVRE
                if (mesaController.buscarMesa(mesaAtiva.getNumeroMesa()).getStatusMesa() == StatusMesa.LIVRE){
                    DialogBoxes.MessageBox("A mesa já está LIVRE", "Erro ao definir");                    
                } else if (mesaController.buscarMesa(mesaAtiva.getNumeroMesa()).getStatusMesa() == StatusMesa.AGUARDANDO_PAGAMENTO){
                    DialogBoxes.MessageBox("A mesa está AGUARDANDO PAGAMENTO! Abra a Comanda e finalize antes!", "Erro ao definir");                                                    
                } else{
                
                } 
                break;                
                
            case 1: //OCUPADA
                if (mesaController.buscarMesa(mesaAtiva.getNumeroMesa()).getStatusMesa() == StatusMesa.LIVRE){
                    mesaAtiva.setStatusMesa(StatusMesa.OCUPADA); 
                    mesaController.atualizarMesa(mesaAtiva);
                    this.dispose();                    
                } else DialogBoxes.MessageBox("A mesa não está LIVRE", "Erro ao definir");                                                     
                break;
            case 2: //AGUARDANDO PAGAMENTO
                if (mesaController.buscarMesa(mesaAtiva.getNumeroMesa()).getStatusMesa() == StatusMesa.OCUPADA){
                    mesaAtiva.setStatusMesa(StatusMesa.AGUARDANDO_PAGAMENTO);
                    mesaController.atualizarMesa(mesaAtiva);
                    this.dispose();                    
                } else DialogBoxes.MessageBox("A mesa não está em uso ou está AGUARDANDO PAGAMENTO!", "Erro ao definir");                                                                                                     
                break;
            default:
                throw new AssertionError();
        }                
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


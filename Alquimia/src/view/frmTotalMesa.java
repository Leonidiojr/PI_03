    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int intComanda = comandaController.buscarComandaAbertaPorMesa(mesaAtiva.getNumeroMesa()).getIdComanda();
        if (intComanda>0 ){            
            application.MainApp.comandaAtiva  =  comandaController.buscarComandaAbertaPorMesa(mesaAtiva.getNumeroMesa());
            application.MainApp.comandaAtiva.setNumeroMesa(mesaAtiva.getNumeroMesa());            
        } else{                        
            intComanda = intComanda + 1;            
            application.MainApp.comandaAtiva  =  comandaController.buscarComandaAbertaPorMesa(mesaAtiva.getNumeroMesa());
            application.MainApp.comandaAtiva.setNumeroMesa(mesaAtiva.getNumeroMesa());                        
            application.MainApp.comandaAtiva.setIdComanda(comandaController.proximaComanda());
            comandaController.criarComanda(comandaAtiva);
        }            
        JFrame frmComanda = new frmComanda();
        frmComanda.setVisible(true);
        this.dispose();                    
                
    }//GEN-LAST:event_jButton1ActionPerformed


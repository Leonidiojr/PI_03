    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        for (Produto produto : produtoController.listarProdutos()) {
            if (produto.getDescricaoProduto()==this.jButton5.getText()){                      
                Pedido pedidoNovo = new Pedido(comandaAtiva.getIdComanda(), produto.getIdProduto(), 1, produto.getValorUnitarioProduto());
                application.Utils.DialogBoxes.MessageBox("Criou", "");
                pedidoController.criarPedido(pedidoNovo);
            }
        }                
        
    }//GEN-LAST:event_jButton5ActionPerformed


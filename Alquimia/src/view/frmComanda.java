    private void carregaMenu() {
        // Obtém os componentes do JPanel
        Component[] components = jPanel1.getComponents();

        // Torna todos os JButton invisíveis inicialmente
        for (Component component : components) {
            if (component instanceof JButton) {
                component.setVisible(false);
            }
        }

        // Atualiza os JButtons visíveis conforme os produtos
        for (Produto produto : ListaProduto) {
            int idProduto = produto.getIdProduto();
            if (idProduto >= 1 && idProduto <= components.length) {
                Component comp = components[idProduto - 1];
                if (comp instanceof JButton) {
                    JButton button = (JButton) comp;
                    button.setText(produto.getDescricaoProduto());
                    button.setVisible(true);
                }
            }
        }       
        
    }

